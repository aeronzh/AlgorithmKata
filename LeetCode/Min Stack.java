public class MinStack {
    Stack<Integer> stack;
    Stack<Integer> min;
    
    /** initialize your data structure here. */
    public MinStack() {
        stack = new Stack<Integer>();
        min = new Stack<Integer>();
    }
    
    public void push(int x) {
        stack.push(x);
        if (!min.isEmpty() && x > min.peek()) {
            min.push(min.peek());
        } else {
            min.push(x);
        }
    }
    
    public void pop() {
        stack.pop();
        min.pop();
    }
    
    public int top() {
        return stack.peek();
    }
    
    public int getMin() {
        return min.peek();
    }
}

